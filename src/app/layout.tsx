"use client";
import "./globals.css";
import type { Metadata } from "next";
import "global";
import { Inter } from "next/font/google";

import { ScrollContext } from "@/context/AppProvider";

import MenuComponent from "@/components/MenuComponent/MenuComponent";
import { useDispatch, useSelector } from "react-redux";
import { RootState } from "@/redux";
import { setLanguage } from "@/redux/reducer/changeLanguageReducer/changeLanguage";
import { useEffect, useState } from "react";
import RollTop from "@/components/RollTop";
import ChatRoll from "@/components/ChatRoll";
import ChangeLanguage from "@/components/ChangeLanguage/ChangeLanguage";
import FooterComponent from "@/components/FooterComponent/FooterComponent";

const inter = Inter({ subsets: ["vietnamese"] });

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  const [reponsiveMobile, setReponsiveMobile] = useState<number>(0);
  useEffect(() => {
    window.addEventListener("load", () => {
      setReponsiveMobile(window.innerWidth);
    });
    window.addEventListener("resize", () => {
      setReponsiveMobile(window.innerWidth);
    });
  }, [reponsiveMobile]);
  return (
    <html lang="en">
      <body suppressHydrationWarning={true} className={inter.className}>
        <ScrollContext>
          <MenuComponent />
          {/* <div>nice</div> */}
          {children}
          {reponsiveMobile >= 1280 ? (
            <>
              <ChangeLanguage />
              <ChatRoll />
              <RollTop />
            </>
          ) : (
            ""
          )}
          <FooterComponent />
        </ScrollContext>
      </body>
    </html>
  );
}
